    private void tabel_pegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_pegawaiMouseClicked
        // TODO add your handling code here:
        int baris = tabel_pegawai.rowAtPoint(evt.getPoint());
        String nip = tabel_pegawai.getValueAt(baris, 1).toString();
        txt_nip.setText(nip);
        
        String nama = tabel_pegawai.getValueAt(baris, 2).toString();
        txt_nama.setText(nama);
        
        String alamat = tabel_pegawai.getValueAt(baris, 3).toString();
        txt_alamat.setText(alamat);
        
        String telpon = tabel_pegawai.getValueAt(baris, 4).toString();
        txt_telpon.setText(telpon);
        
        String bagian = tabel_pegawai.getValueAt(baris, 5).toString();
        txt_bagian.setText(bagian);
    }//GEN-LAST:event_tabel_pegawaiMouseClicked


