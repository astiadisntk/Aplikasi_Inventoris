    private void tampilkan_data(){
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("No.");
    model.addColumn("NIP");
    model.addColumn("Nama Pegawai");
    model.addColumn("Alamat");
    model.addColumn("Telpon");
    model.addColumn("Bagian");
    
    try{
        int no = 1;
        String sql = "SELECT * FROM data_pegawai";
        java.sql.Connection conn = (Connection)Konfig.configDB();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while(res.next()){
            model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
        }
        tabel_pegawai.setModel(model);
    }catch (SQLException e){
        System.out.println("Error : "+e.getMessage());
    }
    }
